const zoneBoundaries = {
  A: {
    y: [0, 1],
    x: [2, 8]
  },
  B: {
    y: [0, 1],
    x: [13, 18]
  },
  C: {
    y: [1, 6],
    x: [19, 20]
  },
  D: {
    y: [8, 9],
    x: [4, 9]
  },
  E: {
    y: [2, 5],
    x: [2, 3]
  }
}

export const isWithinZoneBoundaries = (x, y, zone) => {
  const zoneBoundary = zoneBoundaries[zone];
  const zoneY = zoneBoundary['y']
  const zoneX = zoneBoundary['x']
  return x >= zoneX[0] && x <= zoneX[1] && y >= zoneY[0] && y <= zoneY[1]
}
